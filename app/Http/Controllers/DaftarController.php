        $request->validate(
            [
                'nama' => ['min:2','max:225'],
                'telp' => ['numeric'],
                'email' => ['email:dns'],
                'password' => ['min:8,max:16'],
            ]
        );
        User::create($request->except(['_token','check','button']));
    }

